#1.元组的连接
tuple1 = (1,23,4,2)
tuple2 = (6,7,89,76)
print(tuple1+tuple2)

#2.元组元素的重复 *
print(tuple1 *2)

#连接和重复得到的是一个新的元组


#3.判断指定元素是否在元组中：in
print(2 in tuple1)

#4.元组元素的截取【切片】
#元组名【开始下标，结束下标】,包含开始下标，不包含结束下标
tuple3 = (2,3,5,23,7,54,98,100)
print(tuple3[1:4])
#从指定下标开始截取到结尾，指定下标包含在内
print(tuple3[3:])
#从开头截取到指定位置，不包含指定下标
print(tuple3[:5])










